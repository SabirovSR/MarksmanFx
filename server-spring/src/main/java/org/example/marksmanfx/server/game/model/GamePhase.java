package org.example.marksmanfx.server.game.model;

/**
 * Фазы жизненного цикла игровой комнаты.
 *
 * Мы расширяем оригинальный enum, добавляя фазу LOBBY.
 * LOBBY — это состояние после окончания матча, когда игроки находятся в лобби
 * и зрители могут запросить повышение до статуса PLAYER.
 *
 * Диаграмма переходов:
 *   LOBBY → PLAYING → PAUSE_REQUESTED ⇄ PAUSED
 *                   ↓
 *               FINISHED → LOBBY (после реванша или выхода)
 */
public enum GamePhase {
    /** Комната создана, ждём готовности игроков. Зрители могут стать игроками. */
    LOBBY,
    /** Матч идёт, все игроки получают GameStateMessage каждые 16мс (60 TPS) */
    PLAYING,
    /** Один из игроков запросил паузу, ждём подтверждения от остальных */
    PAUSE_REQUESTED,
    /** Игра на паузе */
    PAUSED,
    /** Матч завершён, победитель определён. Переходим в LOBBY */
    FINISHED
}
